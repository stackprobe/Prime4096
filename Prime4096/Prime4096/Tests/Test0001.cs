		public void Test08()
		{
			BigInteger[] bigPrimes = RES_BIG_PRIMES.Split('\n')
				.Select(line => line.Trim())
				.Where(line => line != "")
				.Select(line => BigInteger.Parse(line))
				.ToArray();

			foreach (BigInteger bigPrime in bigPrimes)
				if (!PrimeUtils.IsPrime(bigPrime))
					throw new Exception(bigPrime.ToString());

			foreach (BigInteger bigPrime1 in bigPrimes)
				foreach (BigInteger bigPrime2 in bigPrimes)
					if (PrimeUtils.IsPrime(bigPrime1 * bigPrime2))
						throw new Exception(bigPrime1 + ", " + bigPrime2);

			foreach (BigInteger bigPrime1 in bigPrimes)
				foreach (int smallPrime in new int[] { 2, 3, 5, 7, 11, 13, 17, 19 })
					if (PrimeUtils.IsPrime(bigPrime1 * smallPrime))
						throw new Exception(bigPrime1 + ", " + smallPrime);
		}
